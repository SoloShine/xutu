# notXisY 句式统一正则(段落级,跨"切句"边界)。
# 四变体全抓:① 不是X是Y(无标点)② 不是X而是Y ③ 不是X，是Y(逗号)④ 不是X。是Y(句号分割)。
# 句号变体收窄:。 须紧邻是(。\\s{0,2}是),免"不是学生。书是借的"误中。
# 注意:须在【整段】上 search(非逐句),否则句号切句后④看不到。
NOTXISY_PATTERN = r"不是[^，。！？\n]{1,15}(?:是|而是|，\s{0,2}(?:是|而是)|。\s{0,2}(?:是|而是))"

SENTENCE_STRUCTURE_PATTERNS = {
    "notXisY": NOTXISY_PATTERN,
}

